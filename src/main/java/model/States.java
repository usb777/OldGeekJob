package model;

public class States 
{

private int st_id;
private String shortname;
private String name;

public int getSt_id()
{
	return st_id;
}
public void setSt_id(int st_id) 
{
	this.st_id = st_id;
}
public String getShortname()
{
	return shortname;
}
public void setShortname(String shortname)
{
	this.shortname = shortname;
}
public String getName()
{
	return name;
}
public void setName(String name) 
{
	this.name = name;
}

	
}
